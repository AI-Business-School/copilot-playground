# Define a function to count from 1 to 10
def count_to_ten():
    # Initialize a counter variable
    counter = 1
    # Loop until the counter reaches 10
    while counter <= 10:
        # Print the current value of the counter
        print(counter)
        # Increment the counter by 1
        counter += 1

# Call the function to count from 1 to 10
count_to_ten()
